#!/usr/bin/python3

"""Define a Square class"""


class Square:
    """
    This class defines a square by its size.

    Attributes:
        __size (int): The size of the square.
    """

    def __init__(self, size):
        """
        Initializes a new instance of the Square class.

        Args:
            size (int): The size of the square.
        """
        self.__size = size
